package com.star.dzcomment.bean;

/**
 * 代理类
 */
public class ProxyServerInfo
{

	private String ip;
	private int port;

	public String getIp()
	{
		return ip;
	}

	public void setIp(String ip)
	{
		this.ip = ip;
	}

	public int getPort()
	{
		return port;
	}

	public void setPort(int port)
	{
		this.port = port;
	}
}
